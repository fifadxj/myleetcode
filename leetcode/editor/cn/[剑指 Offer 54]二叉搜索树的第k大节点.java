/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    int k;
    Integer val = null;
    public int kthLargest(TreeNode root, int k) {
        this.k = k;
        dfs(root);
        return val;
    }

    public void dfs(TreeNode node) {
        if (k == 0) {
            return;
        }

        if (node == null) {
            return;
        }

        dfs(node.right);
        k = k - 1;
        if (k == 0) {
            val = node.val;
        }
        dfs(node.left);
    }
}
