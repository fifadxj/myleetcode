/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean isValidBST(TreeNode root) {
        return isValid(root).valid;
    }

    public Result isValid(TreeNode node) {
        if (node.left == null && node.right == null) {
            return new Result(true, node.val, node.val);
        }

        int min = node.val;
        int max = node.val;
        if (node.left != null) {
            Result result = isValid(node.left);
            if (! result.valid) {
                return new Result(false);
            }
            min = result.min;
            if (node.val <= result.max) {
                return new Result(false);
            }
        }
        if (node.right != null) {
            Result result = isValid(node.right);
            if (! result.valid) {
                return new Result(false);
            }
            max = result.max;
            if (node.val >= result.min) {
                return new Result(false);
            }
        }
        return new Result(true, min, max);
    }

    class Result {
        int min;
        int max;
        boolean valid;
        Result(boolean valid) {
            this.valid = valid;
        }
        Result(boolean valid, int min, int max) {
            this.valid = valid;
            this.min = min;
            this.max = max;
        }
    }
}
